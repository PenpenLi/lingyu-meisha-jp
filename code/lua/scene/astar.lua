Node = class()
function Node:constructor(location, mCost, lid, parent)
  self.location = location -- Where is the node located
  self.mCost = mCost -- Total move cost to reach this node
  self.parent = parent -- Parent node
  self.score = 0 -- Calculated score for this node
  self.lid = lid -- set the location id - unique for each location in the map
end

function Node.__eq(a, b)
  return a.lid == b.lid
end

AStar = class()
function AStar:constructor() 
  self.mh = TiledMapHandler.new()
end

function AStar:_getBestOpenNode()
  local bestNode = nil

  for lid, n in pairs(self.open) do
    if bestNode == nil then
      bestNode = n
    else
      if n.score <= bestNode.score then
        bestNode = n
      end
    end
  end

  return bestNode
end

function AStar:_tracePath(n)
  local nodes = {}
  local totalCost = n.mCost
  local p = n.parent

  table.insert(nodes, 1, n)

  while true do
    if p.parent == nil then
      break
    end
    table.insert(nodes, 1, p)
    p = p.parent
  end

  return Path.new(nodes, totalCost)
end

function AStar:_handleNode(node, goal)
  self.open[node.lid] = nil
  self.closed[node.lid] = node.lid

  assert(node.location ~= nil, 'About to pass a node with nil location to getAdjacentNodes')

  local nodes = self.mh:getAdjacentNodes(node, goal)

  for lid, n in pairs(nodes) do repeat
    if self.mh:locationsAreEqual(n.location, goal) then
      return n
    elseif self.closed[n.lid] ~= nil then -- Alread in close, skip this
      break
    elseif self.open[n.lid] ~= nil then -- Already in open, check if better score   
      local on = self.open[n.lid]

      if n.mCost < on.mCost then
        self.open[n.lid] = nil
        self.open[n.lid] = n
      end
    else -- New node, append to open list
      self.open[n.lid] =  n
    end
  until true end

  return nil
end

function AStar:optimize(path)

  if nil == path then
    return path
  end

  local size = #path.nodes;



  local function getx(i)
    return path.nodes[i].location.x
  end
    
  local function gety(i)
    return path.nodes[i].location.y
  end


  local result = { TiledToPoint(path.nodes[1].location) };
  for i = 2, size-1, 1 do
    local delta_x_1 = getx(i) - getx(i-1);
    local delta_x_2 = getx(i+1) - getx(i);
    local delta_y_1 = gety(i) - gety(i-1);
    local delta_y_2 = gety(i+1) - gety(i);

    if delta_x_1 ~= delta_x_2 or delta_y_1 ~= delta_y_2 then
      --返回转换坐标之后的点
      table.insert(result, TiledToPoint(path.nodes[i].location))

    end
  end


  --把第一个和最后一个点替换成实际坐标
  result[1] = self.oriFrom;
  table.insert(result, self.oriTo)

  return result
end

function AStar:findEndPointWhenUnreachable(fromlocation, tolocation)

  local delta = {
    x = tolocation.x - fromlocation.x;
    y = tolocation.y - fromlocation.y;
  };

  local scale = math.max(math.abs(delta.x), math.abs(delta.y))
  

  for i = scale, 1, -1 do
    local location = {
      x = math.floor(fromlocation.x + delta.x / scale * i);
      y = math.floor(fromlocation.y + delta.y / scale * i);
    }

    if self.mh:available(location) then
      return location;
    end
  end
  return nil;
end

--当前点是否可以经过,阻隔点return false; 
function AStar:canStay(location)
  local pos = PointToTiled(location.x, location.y);
  return self.mh:available(pos);
end

function AStar:findPath(fromlocation, tolocation)
  self.oriFrom = fromlocation;
  self.oriTo = tolocation;


  --转换坐标
  fromlocation = PointToTiled(fromlocation.x, fromlocation.y);
  tolocation = PointToTiled(tolocation.x, tolocation.y);

  if not self.mh:available(tolocation) then
    --不能走的点
    tolocation = self:findEndPointWhenUnreachable(fromlocation, tolocation) 
    if not tolocation then
      return nil
    end
    self.oriTo = TiledToPoint(tolocation)
  end

  --如果to和from在同一个区块内，则直接移动不寻路
  if fromlocation.x == tolocation.x and fromlocation.y == tolocation.y then
    return nil
  end

  self.open = {}
  self.closed = {}

  local goal = tolocation
  local fnode = self.mh:getNode(fromlocation)

  local nextNode = nil

  if fnode ~= nil then
    self.open[fnode.lid] = fnode
    nextNode = fnode
  end  

  while nextNode ~= nil do
    local finish = self:_handleNode(nextNode, goal)

    if finish then
      return self:optimize(self:_tracePath(finish))
    end
    nextNode = self:_getBestOpenNode()
  end

  return nil
end


